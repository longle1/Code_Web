//hàm floor
var a = 3.15;
console.log(Math.floor(a)); //làm tròn số xuống

//hàm ceil
console.log(Math.ceil(a));  //làm tròn lên

//hàm round
console.log(Math.round(a)); // > 0.5 thì làm tròn lên, < 0.5 thì làm tròn xuống

//hàm random
var b = Math.random()* 10; // a + random()* (a+b-1)
console.log(b);
